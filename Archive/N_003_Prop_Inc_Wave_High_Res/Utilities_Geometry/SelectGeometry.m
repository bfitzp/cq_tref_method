function P = SelectGeometry(opt)

switch(opt)
    case 'square'
        P = [
            -1-1i, ...
            1-1i, ...
            1+1i, ...
            -1+1i, ...
            ];
    case 'l_shape'
        P = [
            -1-1i, ...
            1-1i, ...
            1+1i, ...
            0+1i, ...
            -0.00+0.00i ...
            -1+0i, ...
            ];
        
end