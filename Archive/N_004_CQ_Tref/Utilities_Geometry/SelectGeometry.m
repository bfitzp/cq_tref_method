function P = SelectGeometry(opt)

switch(opt)
    case 'square'
        P = [
            -1-1i, ...
            1-1i, ...
            1+1i, ...
            -1+1i, ...
            ];
    case 'l_shape'
        P = [
            -1-1i, ...
            1-1i, ...
            1+1i, ...
            0+1i, ...
            -0.00+0.00i ...
            -1+0i, ...
            ];
    case 'l_shape_nc'
        P = [
            -1-1i, ...
            1-1i, ...
            1+1i, ...
            0+1i, ...
            0+0.3i, ...
           -0.30+0.30i ...
           -0.30+0.0i ...
            -1+0i, ...
            ];      
end